package odev2;

public class Course {

	int id;
	String name;
	String detail;
	int price;
	int instructorId;
	public Course() {
		
	}
	public Course(int id,String name, String detail, int price, int instructorId) {
		
		this.id = id;
		this.name = name;
		this.detail = detail;
		this.price = price;
		this.instructorId = instructorId;
	}
	
	

}
